package day04_Variables;

public class Currencies {

    public static void main(String[] args) {

        int dollar = 1000;

        double lira = dollar * 9.53;
        double euro = dollar * 0.86;
        double jpy = dollar * 0.008761;
        double pound = dollar * 1.3686;
        double peso = dollar * 0.047899;
        double cad = dollar * 0.80916;
        double riyal = dollar * 0.266592;
        double rub = dollar * 0.01403;
        double azn = dollar * 0.588248;
        double czk = dollar * 0.045256;


        System.out.println("czk = " + czk);
        System.out.println("azn = " + azn);
        System.out.println("rub = " + rub);
        System.out.println("riyal = " + riyal);
        System.out.println("cad = " + cad);
        System.out.println("peso = " + peso);
        System.out.println("pound = " + pound);
        System.out.println("jpy = " + jpy);
        System.out.println("euro = " + euro);
        System.out.println("lira = " + lira);
        System.out.println("dollar = " + dollar);




        // jpy, pound, peso, cad, riyal, rub, azn, czk
    }
}
